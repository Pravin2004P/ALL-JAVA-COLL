public class Print_inverted_one_to_Five_in_trangle {
        public static void main(String[] args) {
            int a=5;
            for(int i=0;i<=a;i++) {
                for(int j=0;j<=a-i;j++) {
                    System.out.print(j + " ");
                }
                System.out.println("");
    
            }
              
        }
        
    }
