public class Print_rows_and_columns_stars {
    public static void main(String[] args) {
        int rows = 4;
        int column = 5;
        for(int i=0;i<rows;i++) {
          for(int j=0;j<column;j++) {
            System.out.print("* ");

          }
          System.out.println();
        } 
   }
}       